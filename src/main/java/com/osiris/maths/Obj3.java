package com.osiris.maths;

/**
 * Represents a single 3 dimensional object.
 * Can be a vector or a point for example.
 * Also contains static methods.
 */
public class Obj3 {
    public double x, y, z;

    public Obj3(double x, double y, double z) {
        this.x = x;
        this.y = y;
        this.z = z;
    }
}
